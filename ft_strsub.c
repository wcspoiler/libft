#include "libft.h"

char	*ft_strsub(char const *s, unsigned int start, size_t len)
{
	size_t	i;
	char	*s1;

	i = 0;
	if (NULL == s || (ft_strlen(s) < (size_t)start))
		return (NULL);
	s1 = (char *)malloc(sizeof(char) * (len + 1));
	if (NULL == s1)
		return (NULL);
	while (i < len)
	{
		s1[i] = s[start];
		i++;
		start++;
	}
	s1[i] = '\0';
	return (s1);
}
